function Image_Nor(img) {
    var bandname = img.bandNames();
    var max = img.reduceRegion({
        reducer: ee.Reducer.max(),
        geometry: GZshp,
        scale: 10,
        maxPixels: 1e9
    });
    // print(max.values(bandname))
    var min = img.reduceRegion({
        reducer: ee.Reducer.min(),
        geometry: GZshp,
        scale: 10,
        maxPixels: 1e9
    });
    var Normal = img.expression(
        '(img - min) / (max - min)', {
        'max': ee.Number(max.values(bandname)),
        'min': ee.Number(min.values(bandname)),
        'img': img.select(bandname)
    });
    return Normal;
}

var stackCollection = function (collection) {
    // Create an initial image.
    var first = ee.Image(collection.first()).select([]);

    // Write a function that appends a band to an image.
    var appendBands = function (image, previous) {
        return ee.Image(previous).addBands(image);
    };
    return ee.Image(collection.iterate(appendBands, first));
};
